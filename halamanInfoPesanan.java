    private void kirimNotifikasiKeUser(String namaTarget, String judul, String detail) {
        boolean userDitemukan = false;

        System.out.println("Mencari user dengan nama: " + namaTarget); 
        
        if (userData.users != null) {
            for (userData.User u : userData.users) {
                if (u.username.equalsIgnoreCase(namaTarget)) {
                    if (u.notifications == null) {
                        u.notifications = new java.util.ArrayList<>();
                    }
                    u.notifications.add(new userData.Notification(judul, detail));
                    userDitemukan = true;
                    System.out.println("Notifikasi berhasil disimpan ke user: " + u.username); 
                    break; 
                }
            }
        }
        if (userData.currentUser != null && userData.currentUser.username.equalsIgnoreCase(namaTarget)) {
            if (userData.currentUser.notifications == null) {
                userData.currentUser.notifications = new java.util.ArrayList<>();
            }
            userData.currentUser.notifications.add(new userData.Notification(judul, detail));
        }
        if (!userDitemukan) {           
            System.out.println("GAGAL: User " + namaTarget + " tidak ditemukan di database userData.users.");
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Gagal mengirim notifikasi. User '" + namaTarget + "' tidak ditemukan di database.",
                "Error Sistem", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }
