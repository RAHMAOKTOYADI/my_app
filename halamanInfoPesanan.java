    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        
        if (row >= 0) {
            userData.Appointment app = userData.allAppointments.get(row);
            if (app.status.equalsIgnoreCase("Menunggu")) {
                int response = JOptionPane.showConfirmDialog(this, 
                        "Setujui pesanan ini?\nPasien: " + app.namaPasien, 
                        "Konfirmasi", JOptionPane.YES_NO_OPTION);
                
                if (response == JOptionPane.YES_OPTION) {
                    
                    app.status = "Terkonfirmasi"; 
                    app.pernahDikonfirmasi = true; 
                    
                    loadTableData(); 
                    JOptionPane.showMessageDialog(this, "Status diubah jadi Terkonfirmasi.");
                }
            } 
            else if (app.status.equalsIgnoreCase("Pengajuan Batal")) {
                 int response = JOptionPane.showConfirmDialog(this, 
                        "Setujui pembatalan?", 
                        "Konfirmasi", JOptionPane.YES_NO_OPTION);

                 if (response == JOptionPane.YES_OPTION) {
                    app.status = "Dibatalkan"; 
                    loadTableData();
                 }
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked
