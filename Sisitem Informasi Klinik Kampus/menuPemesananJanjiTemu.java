    private boolean cekValidasiJam(String jamStr, String menitStr) {
        int jam = Integer.parseInt(jamStr);
        int menit = Integer.parseInt(menitStr.trim());
        
        String namaDokter = "";
        if (selectedPanel == panelSmith) namaDokter = "Dr Smith";
        else if (selectedPanel == panelAlex) namaDokter = "Dr Alex";
        else if (selectedPanel == panelAndrew) namaDokter = "Dr Andrew";
        else if (selectedPanel == panelFermino) namaDokter = "Dr Fermino";
        else if (selectedPanel == panelSinta) namaDokter = "Dr Sinta";
        else if (selectedPanel == panelMiska) namaDokter = "Dr Miska";
        else if (selectedPanel == panelAlena) namaDokter = "Dr Alena";
        else if (selectedPanel == panelRobert) namaDokter = "Dr Robert";

        int jamMulai = 0;
        int jamSelesai = 0;

        boolean found = false;
        for (userData.JadwalDokter jd : userData.listJadwal) {
            if (jd.namaDokter.equals(namaDokter)) {              
                jamMulai = Integer.parseInt(jd.jamMulai.substring(0, 2));
                jamSelesai = Integer.parseInt(jd.jamSelesai.substring(0, 2));
                found = true;
                break;
            }
        }
        if (!found) {
            if (selectedPanel == panelAlena || selectedPanel == panelRobert) {
                jamMulai = 16; jamSelesai = 20;
            } else {
                jamMulai = 8; jamSelesai = 15;
            }
        }
        if (jam < jamMulai || jam > jamSelesai) return false;
        if (jam == jamSelesai && menit > 0) return false;
        return true;
    }
