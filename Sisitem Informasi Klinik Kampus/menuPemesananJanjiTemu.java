    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (selectedPanel == null) {
            JOptionPane.showMessageDialog(this, "Silahkan pilih Dokter terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Date tanggalDipilih = jDateChooser1.getDate();
        String jam = jComboBox1.getSelectedItem().toString();
        String menit = jComboBox2.getSelectedItem().toString().trim(); 
        if (tanggalDipilih == null) {
            JOptionPane.showMessageDialog(this, "Mohon pilih tanggal pada kalender!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (jam.equals("Jam") || menit.equals("Menit") || menit.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mohon tentukan Jam dan Menit konsultasi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        SimpleDateFormat sdfHari = new SimpleDateFormat("EEEE", new Locale("id", "ID"));
        String hariPilihanUser = sdfHari.format(tanggalDipilih);
        String hariWajibDokter = getHariWajibDokter();

        if (!hariPilihanUser.equalsIgnoreCase(hariWajibDokter)) {
            JOptionPane.showMessageDialog(this, 
                "Maaf, Dokter ini hanya praktik pada hari " + hariWajibDokter + ".\n" +
                "Anda memilih hari " + hariPilihanUser + ".", 
                "Jadwal Hari Tidak Sesuai", JOptionPane.ERROR_MESSAGE);
            jTextField1.setText("");
            return; 
        }
        boolean isJamValid = cekValidasiJam(jam, menit);

        if (!isJamValid) {
            String jadwalJam = getDokterInfo()[1]; 
            JOptionPane.showMessageDialog(this, 
                "Maaf, Jam yang Anda pilih di luar jam praktik dokter.\n" +
                "Jadwal Praktik: " + jadwalJam + "\n" +
                "Pilihan Anda: " + jam + ":" + menit,
                "Jam Tidak Sesuai", JOptionPane.ERROR_MESSAGE);
            jTextField1.setText(""); 
            return;
        }
        SimpleDateFormat sdfLengkap = new SimpleDateFormat("EEEE, dd MMMM yyyy", new Locale("id", "ID"));
        String strTanggal = sdfLengkap.format(tanggalDipilih);
        String strWaktu = jam + ":" + menit + " WIB";
        String hasilAkhir = strTanggal + " | " + strWaktu;
        jTextField1.setText(hasilAkhir);
        JOptionPane.showMessageDialog(this, "Jadwal Berhasil Disimpan!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String inputLengkap = jTextField1.getText(); 
        String nama = jTextField2.getText();
        String nim = jTextField4.getText();
        String keluhan = jTextArea1.getText();

        if (selectedPanel == null || inputLengkap.isEmpty() || nama.isEmpty() || nim.isEmpty() || keluhan.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Harap lengkapi semua data (Dokter, Jadwal, Nama, NIM, Keluhan)!", "Gagal", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String hariSaja = "";
        String waktuSpesifik = "";

        if (inputLengkap.contains("|")) {
            String[] parts = inputLengkap.split(" \\| "); 
            String bagianTanggal = parts[0]; 
            waktuSpesifik = parts[1];        
            hariSaja = bagianTanggal.split(",")[0];
        } else {
            hariSaja = inputLengkap;
            waktuSpesifik = getDokterInfo()[1]; 
        }

        String namaDokter = getDokterInfo()[0];

        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        model.addRow(new Object[]{
            nama,   
            keluhan,        
            hariSaja,       
            waktuSpesifik,  
            namaDokter,     
            "Menunggu"      
        });

        if (userData.currentUser != null) {
         userData.Appointment newApp = new userData.Appointment();

         newApp.namaPasien = nama;
         newApp.nim = nim;
         newApp.keluhan = keluhan;
         newApp.hari = hariSaja;       
         newApp.waktu = waktuSpesifik; 
         newApp.dokter = namaDokter;
         newApp.status = "Menunggu";   

         userData.currentUser.appointments.add(newApp);

         userData.allAppointments.add(newApp);
         
         if (userData.currentUser != null) {
                String pesanSingkat = "Pesanan Terkirim";
                String detailPesan = "Pesanan janji temu Anda telah berhasil dikirim ke sistem.\nMohon tunggu konfirmasi dokter.";
                userData.currentUser.notifications.add(new userData.Notification(pesanSingkat, detailPesan));
            }
        }

        JOptionPane.showMessageDialog(this, "Janji Temu Berhasil Dibuat!", "Sukses", JOptionPane.INFORMATION_MESSAGE);

        jTextField1.setText("");
        jTextField2.setText("");
        jTextField4.setText("");
        jTextArea1.setText("");
        jDateChooser1.setDate(null);
        jComboBox1.setSelectedIndex(0); 
        jComboBox2.setSelectedIndex(0); 

        if (selectedPanel != null) {
            selectedPanel.setBackground(new java.awt.Color(255, 255, 255));
            selectedPanel = null;
        }
    }//GEN-LAST:event_jButton3ActionPerformed

